#!/usr/bin/env node
// Generates specs/_architecture_/repo-map.json — a machine-readable map of the monorepo
// (plugin roster + test targets + pluginval bundles, DSP layer header counts, dependency
// sources) derived from the filesystem + CMake so it cannot drift from prose docs.
//
//   node tools/gen-repo-map.js            # write the JSON
//   node tools/gen-repo-map.js --check     # exit 1 if the committed file is stale
//
// Deterministic output (sorted keys, no timestamps) so CI can diff-gate it. Node only.

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(REPO_ROOT, 'specs', '_architecture_', 'repo-map.json');

const read = (p) => fs.readFileSync(p, 'utf8');
const exists = (p) => fs.existsSync(p);
const cap = (s) => s.charAt(0).toUpperCase() + s.slice(1);

// --- Plugins: roster from plugins/, test targets from each tests/CMakeLists.txt ----------
const PLUGINS_DIR = path.join(REPO_ROOT, 'plugins');
const pluginDirs = fs
  .readdirSync(PLUGINS_DIR, { withFileTypes: true })
  .filter((e) => e.isDirectory() && e.name !== 'shared')
  .map((e) => e.name)
  .sort();

const plugins = pluginDirs.map((name) => {
  const cmake = path.join(PLUGINS_DIR, name, 'tests', 'CMakeLists.txt');
  let targets = [];
  if (exists(cmake)) {
    targets = [...read(cmake).matchAll(/add_executable\(\s*([a-z0-9_]+_tests)\b/g)].map((m) => m[1]);
    targets = [...new Set(targets)].sort();
  }
  return {
    dir: `plugins/${name}`,
    name: cap(name),
    test_targets: targets,
    pluginval_bundle: `build/windows-x64-release/VST3/Release/${cap(name)}.vst3`,
  };
});

// --- DSP layers: header counts per layer folder -----------------------------------------
const DSP_INC = path.join(REPO_ROOT, 'dsp', 'include', 'krate', 'dsp');
const LAYER_ORDER = ['core', 'primitives', 'processors', 'systems', 'effects'];
const dsp_layers = {};
LAYER_ORDER.forEach((layer, i) => {
  const dir = path.join(DSP_INC, layer);
  let headers = 0;
  if (exists(dir)) {
    const walk = (d) => {
      for (const e of fs.readdirSync(d, { withFileTypes: true })) {
        if (e.isDirectory()) walk(path.join(d, e.name));
        else if (e.name.endsWith('.h')) headers++;
      }
    };
    walk(dir);
  }
  dsp_layers[layer] = { index: i, folder: `dsp/include/krate/dsp/${layer}`, header_count: headers };
});

// --- Dependencies: submodules + FetchContent + vendored ---------------------------------
const deps = [];

// git submodules from .gitmodules
const gm = path.join(REPO_ROOT, '.gitmodules');
if (exists(gm)) {
  for (const m of read(gm).matchAll(/path\s*=\s*(\S+)/g)) {
    deps.push({ name: path.basename(m[1]), source: 'submodule', location: m[1] });
  }
}

// FetchContent declarations in the root CMakeLists.txt
const rootCmake = read(path.join(REPO_ROOT, 'CMakeLists.txt'));
for (const m of rootCmake.matchAll(/FetchContent_Declare\(\s*([A-Za-z0-9_.]+)/g)) {
  deps.push({ name: m[1], source: 'fetchcontent', location: 'build/_deps' });
}

// vendored: subdirs of extern/ that are not submodules
const EXTERN = path.join(REPO_ROOT, 'extern');
if (exists(EXTERN)) {
  const submodulePaths = new Set(deps.filter((d) => d.source === 'submodule').map((d) => d.location));
  for (const e of fs.readdirSync(EXTERN, { withFileTypes: true })) {
    if (!e.isDirectory()) continue;
    const rel = `extern/${e.name}`;
    if (!submodulePaths.has(rel)) deps.push({ name: e.name, source: 'vendored', location: rel });
  }
}

deps.sort((a, b) => a.source.localeCompare(b.source) || a.name.localeCompare(b.name));

const map = {
  _comment: 'Generated by tools/gen-repo-map.js — do not edit by hand.',
  build: {
    cmake_windows: '/c/Program Files/CMake/bin/cmake.exe',
    release_preset: 'windows-x64-release',
    dev_preset: 'windows-ninja-dev',
    test_bin_dir: 'build/windows-x64-release/bin/Release',
  },
  plugins,
  dsp_layers,
  dependencies: deps,
};

const output = JSON.stringify(map, null, 2) + '\n';

if (process.argv.includes('--check')) {
  const current = exists(OUT_FILE) ? read(OUT_FILE) : '';
  if (current !== output) {
    console.error('specs/_architecture_/repo-map.json is stale — run: node tools/gen-repo-map.js');
    process.exit(1);
  }
  console.log('repo-map.json is up to date.');
} else {
  fs.writeFileSync(OUT_FILE, output);
  console.log(`Wrote specs/_architecture_/repo-map.json (${plugins.length} plugins, ${deps.length} deps).`);
}
