#!/usr/bin/env node
// Generates specs/INDEX.md — a subsystem-grouped index of every feature spec so an
// agent can map a subsystem/plugin to its governing spec without folder guessing.
//
// Deterministic (no timestamps / no absolute paths) so CI can diff-gate it:
//   node tools/gen-specs-index.js            # write specs/INDEX.md
//   node tools/gen-specs-index.js --check     # exit 1 if the committed file is stale
//
// Node only (project rule: helper scripts are Node, not Python).

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const SPECS_DIR = path.join(REPO_ROOT, 'specs');
const OUT_FILE = path.join(SPECS_DIR, 'INDEX.md');

// Subsystem inference: first keyword found in the spec's slug wins; order matters.
const SUBSYSTEMS = [
  ['membrum', 'Membrum'],
  ['gradus', 'Gradus'],
  ['ruinae', 'Ruinae'],
  ['innexus', 'Innexus'],
  ['disrumpo', 'Disrumpo'],
  ['iterum', 'Iterum'],
  ['arp', 'Arpeggiator (shared)'],
  ['harmoniz', 'DSP / Harmonizer'],
  ['spectral', 'DSP / Spectral'],
  ['filter', 'DSP / Filters'],
  ['oscillat', 'DSP / Oscillators'],
  ['grain', 'DSP / Granular'],
  ['dsp', 'DSP / Core'],
];

function classify(slug) {
  const s = slug.toLowerCase();
  for (const [kw, label] of SUBSYSTEMS) if (s.includes(kw)) return label;
  return 'Other';
}

function walkForSpecs(dir) {
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const full = path.join(dir, entry.name);
    if (fs.existsSync(path.join(full, 'spec.md'))) {
      out.push(path.join(full, 'spec.md'));
    }
    out.push(...walkForSpecs(full));
  }
  return out;
}

function firstH1(file) {
  const lines = fs.readFileSync(file, 'utf8').split(/\r?\n/);
  for (const l of lines) {
    const m = l.match(/^#\s+(.*)$/);
    if (m) return m[1].replace(/^Feature Specification:\s*/i, '').trim();
  }
  return '(untitled)';
}

function locationOf(relDir) {
  if (relDir.startsWith('_archive2_')) return 'archive2';
  if (relDir.startsWith('_archive_')) return 'archive';
  return 'active';
}

const specs = walkForSpecs(SPECS_DIR).map((file) => {
  const dir = path.dirname(file);
  const rel = path.relative(SPECS_DIR, dir).split(path.sep).join('/');
  const slug = path.basename(dir);
  const idMatch = slug.match(/^(\d+)/);
  return {
    id: idMatch ? parseInt(idMatch[1], 10) : null,
    slug,
    title: firstH1(file),
    rel,
    relFromRoot: 'specs/' + path.relative(SPECS_DIR, file).split(path.sep).join('/'),
    location: locationOf(rel),
    subsystem: classify(slug),
  };
});

// Group by subsystem, then split active vs archived within each group.
const groups = {};
for (const s of specs) (groups[s.subsystem] ||= []).push(s);

const groupOrder = [...new Set(SUBSYSTEMS.map((s) => s[1])), 'Other'];
const byIdDesc = (a, b) => (b.id || 0) - (a.id || 0) || a.slug.localeCompare(b.slug);

let md = '';
md += '# Specs Index\n\n';
md += '> Generated by `tools/gen-specs-index.js` — do not edit by hand. Run the generator to refresh.\n\n';
md += `Total spec dirs: **${specs.length}** `;
md += `(active: ${specs.filter((s) => s.location === 'active').length}, `;
md += `archived: ${specs.filter((s) => s.location !== 'active').length}). `;
md += 'Both archives restart numbering at 001, so IDs are unique only within a location.\n\n';

for (const g of groupOrder) {
  const items = groups[g];
  if (!items || !items.length) continue;
  md += `## ${g}\n\n`;
  const active = items.filter((s) => s.location === 'active').sort(byIdDesc);
  const archived = items.filter((s) => s.location !== 'active').sort(byIdDesc);
  if (active.length) {
    md += '| ID | Title | Path |\n|----|-------|------|\n';
    for (const s of active) md += `| ${s.id ?? '—'} | ${s.title} | [${s.rel}](${s.rel}/spec.md) |\n`;
    md += '\n';
  }
  if (archived.length) {
    md += `<details><summary>Archived (${archived.length})</summary>\n\n`;
    md += '| ID | Title | Path |\n|----|-------|------|\n';
    for (const s of archived) md += `| ${s.id ?? '—'} | ${s.title} | [${s.rel}](${s.rel}/spec.md) |\n`;
    md += '\n</details>\n\n';
  }
}

const output = md.trimEnd() + '\n';

if (process.argv.includes('--check')) {
  const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, 'utf8') : '';
  if (current !== output) {
    console.error('specs/INDEX.md is stale — run: node tools/gen-specs-index.js');
    process.exit(1);
  }
  console.log('specs/INDEX.md is up to date.');
} else {
  fs.writeFileSync(OUT_FILE, output);
  console.log(`Wrote specs/INDEX.md (${specs.length} specs).`);
}
